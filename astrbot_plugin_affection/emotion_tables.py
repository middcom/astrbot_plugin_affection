def _map_value_to_bracket(value: float) -> float:
    brackets = [0.0, 12.5, 25.0, 37.5, 50.0]
    for b in brackets:
        if value <= b:
            return b
    return 50.0


def _get_affection_level(affection: float) -> int:
    if affection < 12.5:
        return 0
    elif affection < 37.5:
        return 25
    elif affection < 62.5:
        return 50
    elif affection < 87.5:
        return 75
    else:
        return 100


